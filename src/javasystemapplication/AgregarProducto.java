    private void confirmarGuardarProducto() {
        String nombreProducto = txtNombreProducto.getText().trim();

        if (nombreProducto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del producto no puede estar vac铆o.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Mostrar nombre del producto en el cuadro de confirmaci贸n
        int confirmacion = JOptionPane.showConfirmDialog(this,
                "驴Est谩s seguro de que deseas guardar el producto: \n" + nombreProducto + "?",
                "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (confirmacion == JOptionPane.YES_OPTION) {
            boolean exito = productoDAO.agregarProducto(nombreProducto);
            if (exito) {
                JOptionPane.showMessageDialog(this, "Producto agregado correctamente.", "xito", JOptionPane.INFORMATION_MESSAGE);
                dispose();
                new PantallaProductos().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

